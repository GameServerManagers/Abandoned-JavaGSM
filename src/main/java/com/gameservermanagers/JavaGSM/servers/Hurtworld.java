package com.gameservermanagers.JavaGSM.servers;

import com.gameservermanagers.JavaGSM.ServerInstaller;
import com.gameservermanagers.JavaGSM.util.SteamcmdUtil;

import java.io.File;

@SuppressWarnings("unused")
public class Hurtworld implements ServerInstaller {

    public static void install(File destination) {
        boolean installedSuccessfully = SteamcmdUtil.installApp("anonymous", destination, "405100");
        System.out.println(installedSuccessfully
                ? "Finished installing Hurtworld server. Start it with the -s flag."
                : "Failed installing Hurtworld server. See above for errors generated by SteamCMD.")
        ;
        System.out.println();
    }

}
